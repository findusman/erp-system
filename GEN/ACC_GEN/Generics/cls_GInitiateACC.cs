        public  string  initiateCAOPlan(DataSet pCAOPlansDataSet)
        {


            DataTable dt_temp = pCAOPlansDataSet.Tables[0]; 

            cls_ACCGlobalClass.GV_ID = Convert.ToInt32(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_ID"].ToString());
            cls_ACCGlobalClass.GV_planName = Convert.ToString(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_planName"].ToString());
            cls_ACCGlobalClass.GV_maxLevel = Convert.ToInt32(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_maxLevel"].ToString());
            cls_ACCGlobalClass.GV_format = Convert.ToString(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_format"].ToString());
            cls_ACCGlobalClass.GV_transactionLevel = Convert.ToInt32(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_transactionLevel"].ToString());
            cls_ACCGlobalClass.GV_isType = Convert.ToBoolean(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_isType"].ToString());
            cls_ACCGlobalClass.GV_areAllFixed = Convert.ToBoolean(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_areAllFixed"].ToString());
            cls_ACCGlobalClass.GV_areAllAutoGenerated = Convert.ToBoolean(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_areAllAutoGenerated"].ToString());
            cls_ACCGlobalClass.GV_leveSeperator = Convert.ToChar(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_leveSeperator"].ToString());
            cls_ACCGlobalClass.GV_description = Convert.ToString(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_description"].ToString());
            cls_ACCGlobalClass.GV_isActive = Convert.ToBoolean(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_isActive"].ToString());
            cls_ACCGlobalClass.GV_isTransactionFixed = Convert.ToBoolean(dt_temp.Rows[0]["TBL_ACC_PLAN_MAIN_isTransactionFixed"].ToString());
            
            cls_ACCGlobalClass.GT_LevelInformation = pCAOPlansDataSet.Tables[1].Copy();
            cls_ACCGlobalClass.addPerLevelFormat();


            return "ok";


        }


