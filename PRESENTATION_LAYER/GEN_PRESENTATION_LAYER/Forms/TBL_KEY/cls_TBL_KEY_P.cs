        public string valueGetting(bool pIs_Delete)
        {

            try
            {

                string validate_value = "";

                if (!validate(out validate_value, pIs_Delete))
                {

                    if (validate_value == "N")
                        return "V_P";
                    else
                        return validate_value;

                }

                if (pIs_Delete)
                {
                    obj_cls_TBL_KEY.STATUS = "D";
                    obj_cls_TBL_KEY.KEY_ID = Convert.ToString(obj_frm_TBL_KEY.TextEdit_KEY_ID.EditValue);
                }
                {

                    obj_cls_TBL_KEY.Is_Auto_Generated = obj_frm_TBL_KEY.CheckEdit_Is_AutoGenegereted.Checked;

                    if (obj_frm_TBL_KEY.DBStatus == 'I')
                    {

                        if (!obj_frm_TBL_KEY.CheckEdit_Is_AutoGenegereted.Checked)
                            obj_cls_TBL_KEY.KEY_ID = Convert.ToString(obj_frm_TBL_KEY.TextEdit_KEY_ID.EditValue);
                    }
                    else
                        obj_cls_TBL_KEY.KEY_ID = Convert.ToString(obj_frm_TBL_KEY.TextEdit_KEY_ID.EditValue);


                    obj_cls_TBL_KEY.KEY_key = obj_frm_TBL_KEY.TextEdit_KEY_key.EditValue.ToString().Trim();
                    obj_cls_TBL_KEY.KEY_value = obj_frm_TBL_KEY.TextEdit_KEY_value.EditValue.ToString().Trim();
                    obj_cls_TBL_KEY.KEY_types = obj_frm_TBL_KEY.GridLookUpEdit_KEY_types.EditValue.ToString().Trim();
                    obj_cls_TBL_KEY.KEY_extraString1 = obj_frm_TBL_KEY.TextEdit_KEY_extraString1.EditValue.ToString().Trim();
                    obj_cls_TBL_KEY.KEY_KEY_extraString2 = obj_frm_TBL_KEY.TextEdit_KEY_KEY_extraString2.EditValue.ToString().Trim();
                    obj_cls_TBL_KEY.KEY_extraBit1 = obj_frm_TBL_KEY.CheckEdit_KEY_extraBit1.Checked;
                    obj_cls_TBL_KEY.KEY_KEY_extraBit2 = obj_frm_TBL_KEY.CheckEdit_KEY_KEY_extraBit2.Checked;
                    obj_cls_TBL_KEY.KEY_description = obj_frm_TBL_KEY.MemoEdit_KEY_description.EditValue.ToString().Trim();


                }



            }
            catch (Exception ex)
            {

                return ex.Message;

            }

            return "ok";

        }
