            public bool validate(out string value, bool pIs_Delete)
            {
                  if (pIs_Delete)
                  {

                        if (!isAllowedDeletion)
                        {
                              value = obj_cls_MessageBox.error_delete_rights;
                              return false;

                        }

                        if (obj_frm_TBL_CMP.DBStatus == 'I')
                        {
                              value = obj_cls_MessageBox.error_selection_msg;
                              return false;
                        }

                  }
                  else
                  {
                        if (obj_frm_TBL_CMP.DBStatus == 'I')
                        {
                              if (!isAllowedInsertion)
                              {
                                    value = obj_cls_MessageBox.error_insert_rights;
                                    return false;

                              }

                        }
                        if (obj_frm_TBL_CMP.DBStatus == 'U')
                        {
                              if (!isAllowedUpdation)
                              {
                                    value = obj_cls_MessageBox.error_update_rights;
                                    return false;

                              }

                        }



                        if (Convert.ToString(obj_frm_TBL_CMP.TextEdit_CMP_name.EditValue) == "")
                        {
                              GEN.GEN_GEN.GenericClasses.Appearance.cls_Appearance.applyFocusAppreanceTextEdit(obj_frm_TBL_CMP.TextEdit_CMP_name);
                              value = "V_P";
                              return false;
                        }
                        if (Convert.ToString(obj_frm_TBL_CMP.TextEdit_USERS_loginID.EditValue) == "")
                        {
                              GEN.GEN_GEN.GenericClasses.Appearance.cls_Appearance.applyFocusAppreanceTextEdit(obj_frm_TBL_CMP.TextEdit_USERS_loginID);
                              value = "V_P";
                              return false;
                        }
                        if (Convert.ToString(obj_frm_TBL_CMP.TextEdit_USERS_password.EditValue) == "")
                        {
                              GEN.GEN_GEN.GenericClasses.Appearance.cls_Appearance.applyFocusAppreanceTextEdit(obj_frm_TBL_CMP.TextEdit_USERS_password);
                              value = "V_P";
                              return false;
                        }
                        //if (Convert.ToString(obj_frm_TBL_CMP.GridLookUpEdit_CMP_adminUserID.EditValue) == "")
                        //{
                        //      GEN.GEN_GEN.GenericClasses.Appearance.cls_Appearance.applyFocusAppreanceTextEdit(obj_frm_TBL_CMP.GridLookUpEdit_CMP_adminUserID);
                        //      value = "V_P";
                        //      return false;
                        //}


                  }

                  if (obj_frm_TBL_CMP.DBStatus == 'U')
                        if (Convert.ToString(obj_frm_TBL_CMP.TextEdit_CMP_maxID.EditValue) == "")
                        {
                              value = obj_cls_MessageBox.error_ID_msg;
                              return false;
                        }
                  if (obj_frm_TBL_CMP.DBStatus == 'I')
