            public string valueGetting(bool pIs_Delete)
            {

                  try
                  {

                        string validate_value = "";

                        if (!validate(out validate_value, pIs_Delete))
                        {

                              if (validate_value == "N")
                                    return "V_P";
                              else
                                    return validate_value;

                        }

                        if (pIs_Delete)
                        {
                              obj_cls_TBL_CMP.STATUS = "D";
                              obj_cls_TBL_CMP.CMP_ID = Convert.ToString(obj_frm_TBL_CMP.TextEdit_CMP_maxID.EditValue);
                        }
                        {

                              obj_cls_TBL_CMP.Is_Auto_Generated = obj_frm_TBL_CMP.CheckEdit_Is_AutoGenegereted.Checked;

                              if (obj_frm_TBL_CMP.DBStatus == 'I')
                              {

                                    if (!obj_frm_TBL_CMP.CheckEdit_Is_AutoGenegereted.Checked)
                                          obj_cls_TBL_CMP.CMP_ID = Convert.ToString(obj_frm_TBL_CMP.TextEdit_CMP_maxID.EditValue);
                              }
                              else
                                    obj_cls_TBL_CMP.CMP_ID = Convert.ToString(obj_frm_TBL_CMP.TextEdit_CMP_maxID.EditValue);


                              obj_cls_TBL_CMP.CMP_name = obj_frm_TBL_CMP.TextEdit_CMP_name.EditValue.ToString().Trim();
                              obj_cls_TBL_CMP.CMP_address = obj_frm_TBL_CMP.TextEdit_CMP_address.EditValue.ToString().Trim();
                              obj_cls_TBL_CMP.CMP_adminUserID = "";//obj_frm_TBL_CMP.GridLookUpEdit_CMP_adminUserID.EditValue.ToString().Trim();
                              obj_cls_TBL_CMP.CMP_phoneNumber = obj_frm_TBL_CMP.TextEdit_CMP_phoneNumber.EditValue.ToString().Trim();
                              obj_cls_TBL_CMP.CMP_numberOfBranches = Convert.ToInt32(obj_frm_TBL_CMP.TextEdit_CMP_numberOfBranches.EditValue.ToString().Trim() == "" ? "0" : obj_frm_TBL_CMP.TextEdit_CMP_numberOfBranches.EditValue.ToString().Trim());
                              obj_cls_TBL_CMP.CMP_narration = obj_frm_TBL_CMP.TextEdit_CMP_narration.EditValue.ToString().Trim();
                              obj_cls_TBL_CMP.USERS_loginID = obj_frm_TBL_CMP.TextEdit_USERS_loginID.EditValue.ToString().Trim();
                              obj_cls_TBL_CMP.USERS_password = obj_frm_TBL_CMP.TextEdit_USERS_password.EditValue.ToString().Trim();
                              obj_cls_TBL_CMP.USERS_ID = TBL_USERS_ID;


                        }



                  }
                  catch (Exception ex)
                  {

                        return ex.Message;

                  }

                  return "ok";

            }
