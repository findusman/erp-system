            public bool validate(out string value, bool pIs_Delete)
            {
                  if (pIs_Delete)
                  {

                        if (!isAllowedDeletion)
                        {
                              value = obj_cls_MessageBox.error_delete_rights;
                              return false;

                        }

                        if (obj_frm_TBL_VCH_MAIN.DBStatus == 'I')
                        {
                              value = obj_cls_MessageBox.error_selection_msg;
                              return false;
                        }

                  }
                  else
                  {
                        if (obj_frm_TBL_VCH_MAIN.DBStatus == 'I')
                        {
                              if (!isAllowedInsertion)
                              {
                                    value = obj_cls_MessageBox.error_insert_rights;
                                    return false;

                              }

                        }
                        if (obj_frm_TBL_VCH_MAIN.DBStatus == 'U')
                        {
                              if (!isAllowedUpdation)
                              {
                                    value = obj_cls_MessageBox.error_update_rights;
                                    return false;

                              }

                        }


                        double tmpDebit = Convert.ToDouble(obj_frm_TBL_VCH_MAIN.GridView_TBL_VCH_DETAILS.Columns[cls_CTBL_VCH_MAIN.VCH_DETAILS_debit].SummaryItem.SummaryValue.ToString());
                        double tmpCredit = Convert.ToDouble(obj_frm_TBL_VCH_MAIN.GridView_TBL_VCH_DETAILS.Columns[cls_CTBL_VCH_MAIN.VCH_DETAILS_credit].SummaryItem.SummaryValue.ToString());
                        string tmpNumericFormat = obj_frm_TBL_VCH_MAIN.GridView_TBL_VCH_DETAILS.Columns[cls_CTBL_VCH_MAIN.VCH_DETAILS_credit].DisplayFormat.FormatString;
                        string tmpStrDebit = tmpDebit.ToString(tmpNumericFormat);
                        string tmpStrCredit = tmpCredit.ToString(tmpNumericFormat);


                        if (tmpStrDebit != tmpStrCredit)
                        {
                              value = obj_cls_MessageBox.error_debit_credit_not_equal;
                              return false;
                        }


                  }

                  if (obj_frm_TBL_VCH_MAIN.DBStatus == 'U')
                        if (Convert.ToString(obj_frm_TBL_VCH_MAIN.TextEdit_VCH_ID.EditValue) == "")
                        {
                              value = obj_cls_MessageBox.error_ID_msg;
                              return false;
                        }
                  if (obj_frm_TBL_VCH_MAIN.DBStatus == 'I')
                        if (!obj_frm_TBL_VCH_MAIN.CheckEdit_Is_AutoGenegereted.Checked && Convert.ToString(obj_frm_TBL_VCH_MAIN.TextEdit_VCH_ID.EditValue) == "")
                        {
                              value = obj_cls_MessageBox.error_ID_msg;
                              return false;
                        }


                  value = "N";
                  return true;
            }




