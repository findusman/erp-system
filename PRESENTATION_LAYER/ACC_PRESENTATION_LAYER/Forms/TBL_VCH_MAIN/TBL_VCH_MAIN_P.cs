            public void selection(string pStatus, string pId)
            {

                  if (pStatus == "N")
                  {
                        if (pId == "")
                        {
                              obj_cls_DataSet_ACC.f_TBL_VCH_MAIN("L", pId,tmpVCHPrefix);
                              DataSet ds = obj_cls_DataSet_ACC.g_TBL_VCH_MAIN;
                              if (ds == null)
                              {
                                    obj_cls_MessageBox.MessageBoxStatic("BLL_E");
                                    Referesh("False");
                                    return;

                              }

                              dt_Datanavigator = ds.Tables[0].Copy();
                              obj_frm_TBL_VCH_MAIN.DataNavigator_Navigate.DataSource = dt_Datanavigator;
                        }
                        else
                        {

                              string ID = dt_Datanavigator.Rows[Convert.ToInt32(pId)][cls_CTBL_VCH_MAIN.priVCH_ID].ToString();
                              selection("V", ID);
                        }
                  }
                  else if (pStatus == "A")
                  {

                        obj_cls_DataSet_ACC.f_TBL_VCH_MAIN("A", pId, tmpVCHPrefix);
                        DataSet ds = obj_cls_DataSet_ACC.g_TBL_VCH_MAIN;

                        if (ds == null)
                        {
                              obj_cls_MessageBox.MessageBoxStatic("BLL_E");
                              Referesh("False");
                              return;

                        }


                        obj_frlLOOKUP = new GEN.GEN_GEN.Look.frlLOOKUP(ds, 4, true, "150,150,150,150,150,150,150,150,150", 1);
                        obj_frlLOOKUP.ShowDialog();
                        string value = GEN.GEN_GEN.Look.frlLOOKUP.value;
                        if (value != "")
                        {
                              string[] cols_values = value.Split('|');
                              obj_frm_TBL_VCH_MAIN.CheckEdit_navigate.Checked = false;
                              selection("V", cols_values[0]);
                        }

                  }
                  else if (pStatus == "G")
                  {
                        obj_cls_DataSet_ACC.f_TBL_VCH_MAIN("G", pId, tmpVCHPrefix);
                        DataSet ds = obj_cls_DataSet_ACC.g_TBL_VCH_MAIN;
                        if (ds == null)
                        {
                              obj_cls_MessageBox.MessageBoxStatic("BLL_E");
                              Referesh("False");
                              return;

                        }

                        DT_dataTableForGrid = ds.Tables[0].Copy();
                  }
                  else
                  {

                        //obj_frm_TBL_VCH_MAIN.maxID = obj_frm_TBL_VCH_MAIN.TextEdit_VCH_ID.Text = pId;
                        obj_cls_DataSet_ACC.f_TBL_VCH_MAIN("V", pId, tmpVCHPrefix);
                        DataSet ds = obj_cls_DataSet_ACC.g_TBL_VCH_MAIN;
                        string executeFunctions = "";

                        if (!GEN.GEN_GEN.GenericClasses.DataTables.cls_NativDataSet.checkIsNullIsNoTableIsTableEmpty(ds, 0))
                        {
                              executeFunctions = valueSetting(ds);
                        }
                        else
                        {

                              obj_cls_MessageBox.MessageBoxStatic("TB_E");
                              Referesh("False");
                              return;

                        }


                        if (executeFunctions != "ok")
                        {
                              obj_cls_MessageBox.MessageBoxDynamics(executeFunctions, "S_E");
                              return;

                              Referesh("False");


                        }


                  }

            }

