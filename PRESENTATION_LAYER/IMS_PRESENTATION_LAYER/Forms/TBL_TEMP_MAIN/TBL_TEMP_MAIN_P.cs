            public string valueGetting(bool pIs_Delete)
            {

                  try
                  {

                        string validate_value = "";

                        if (!validate(out validate_value, pIs_Delete))
                        {

                              if (validate_value == "N")
                                    return "V_P";
                              else
                                    return validate_value;

                        }

                        if (pIs_Delete)
                        {
                              obj_cls_TBL_TEMP_MAIN.STATUS = "D";
                              obj_cls_TBL_TEMP_MAIN.TEMP_MAIN_ID = Convert.ToString(obj_frm_TBL_TEMP_MAIN.TextEdit_TEMP_MAIN_ID.EditValue);
                        }
                        {
                              obj_cls_TBL_TEMP_MAIN.dt_Detail = DT_dataTableForGrid;
                              obj_cls_TBL_TEMP_MAIN.Is_Auto_Generated = obj_frm_TBL_TEMP_MAIN.CheckEdit_Is_AutoGenegereted.Checked;

                              if (obj_frm_TBL_TEMP_MAIN.DBStatus == 'I')
                              {

                                    if (!obj_frm_TBL_TEMP_MAIN.CheckEdit_Is_AutoGenegereted.Checked)
                                          obj_cls_TBL_TEMP_MAIN.TEMP_MAIN_ID = Convert.ToString(obj_frm_TBL_TEMP_MAIN.TextEdit_TEMP_MAIN_ID.EditValue);
                              }
                              else
                                    obj_cls_TBL_TEMP_MAIN.TEMP_MAIN_ID = Convert.ToString(obj_frm_TBL_TEMP_MAIN.TextEdit_TEMP_MAIN_ID.EditValue);


                              obj_cls_TBL_TEMP_MAIN.TEMP_MAIN_name = obj_frm_TBL_TEMP_MAIN.TextEdit_TEMP_MAIN_name.EditValue.ToString().Trim();


                        }



                  }
                  catch (Exception ex)
                  {

                        return ex.Message;

                  }

                  return "ok";

            }
