        public string valueGetting(bool pIs_Delete)
        {

            try
            {

                string validate_value = "";

                if (!validate(out validate_value, pIs_Delete))
                {

                    if (validate_value == "N")
                        return "V_P";
                    else
                        return validate_value;

                }

                if (pIs_Delete)
                {
                    obj_cls_TBL_DEPARTMENTS.STATUS = "D";
                    obj_cls_TBL_DEPARTMENTS.DEPARTMENT_ID = Convert.ToString(obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_ID.EditValue);
                }
                {

                    obj_cls_TBL_DEPARTMENTS.Is_Auto_Generated = obj_frm_TBL_DEPARTMENTS.CheckEdit_Is_AutoGenegereted.Checked;

                    if (obj_frm_TBL_DEPARTMENTS.DBStatus == 'I')
                    {

                        if (!obj_frm_TBL_DEPARTMENTS.CheckEdit_Is_AutoGenegereted.Checked)
                            obj_cls_TBL_DEPARTMENTS.DEPARTMENT_ID = Convert.ToString(obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_ID.EditValue);
                    }
                    else
                        obj_cls_TBL_DEPARTMENTS.DEPARTMENT_ID = Convert.ToString(obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_ID.EditValue);


                    obj_cls_TBL_DEPARTMENTS.DEPARTMENT_name = obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_name.EditValue.ToString().Trim();
                    obj_cls_TBL_DEPARTMENTS.DEPARTMENT_narration = obj_frm_TBL_DEPARTMENTS.MemoEdit_DEPARTMENT_narration.EditValue.ToString().Trim();

                    obj_cls_TBL_DEPARTMENTS.DEPARTMENT_parentID = obj_frm_TBL_DEPARTMENTS.checkEdit_DEPARTMENT_isParent.Checked ? obj_frm_TBL_DEPARTMENTS.GridLookUpEdit_DEPARTMENT_parentID.EditValue.ToString() : "N";
                    obj_cls_TBL_DEPARTMENTS.DEPARTMENT_isParent = !obj_frm_TBL_DEPARTMENTS.checkEdit_DEPARTMENT_isParent.Checked;
                    obj_cls_TBL_DEPARTMENTS.DEPARTMENT_stockLevel = obj_frm_TBL_DEPARTMENTS.ComboBoxEdit_Stocklevel.SelectedIndex +1;
                    obj_cls_TBL_DEPARTMENTS.DEPARTMENT_stockInLtr = obj_frm_TBL_DEPARTMENTS.checkEdit_DEPARTMENT_stockIsLtr.Checked;



                }



            }
            catch (Exception ex)
            {

                return ex.Message;

            }

            return "ok";

        }

       

        public string valueSetting(DataSet ds)
        {

            try
            {

                DataTable dt_Main = ds.Tables[0];

                obj_frm_TBL_DEPARTMENTS.CheckEdit_Is_AutoGenegereted.Checked = (Boolean)dt_Main.Rows[0][GEN.GEN_GEN.GenericClasses.cls_GENGlobalClass.Is_Auto_Generated];



                obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_name.EditValue = dt_Main.Rows[0][BLL.IMS_BLL.TBL_DEPARTMENTS.cls_CTBL_DEPARTMENTS.DEPARTMENT_name];
                obj_frm_TBL_DEPARTMENTS.MemoEdit_DEPARTMENT_narration.EditValue = dt_Main.Rows[0][BLL.IMS_BLL.TBL_DEPARTMENTS.cls_CTBL_DEPARTMENTS.DEPARTMENT_narration];
                obj_cls_TBL_DEPARTMENTS.DEPARTMENT_COA = dt_Main.Rows[0][BLL.IMS_BLL.TBL_DEPARTMENTS.cls_CTBL_DEPARTMENTS.DEPARTMENT_COA].ToString();
                obj_frm_TBL_DEPARTMENTS.checkEdit_DEPARTMENT_isParent.EditValue = !(bool)dt_Main.Rows[0][BLL.IMS_BLL.TBL_DEPARTMENTS.cls_CTBL_DEPARTMENTS.DEPARTMENT_isParent];

                if (obj_frm_TBL_DEPARTMENTS.checkEdit_DEPARTMENT_isParent.Checked)
                      obj_frm_TBL_DEPARTMENTS.GridLookUpEdit_DEPARTMENT_parentID.EditValue = dt_Main.Rows[0][BLL.IMS_BLL.TBL_DEPARTMENTS.cls_CTBL_DEPARTMENTS.DEPARTMENT_parentID].ToString(); ;
                obj_frm_TBL_DEPARTMENTS.ComboBoxEdit_Stocklevel.SelectedIndex = Convert.ToInt16( dt_Main.Rows[0][BLL.IMS_BLL.TBL_DEPARTMENTS.cls_CTBL_DEPARTMENTS.DEPARTMENT_stockLevel].ToString()) - 1;
                obj_frm_TBL_DEPARTMENTS.checkEdit_DEPARTMENT_stockIsLtr.Checked = (bool)dt_Main.Rows[0][BLL.IMS_BLL.TBL_DEPARTMENTS.cls_CTBL_DEPARTMENTS.DEPARTMENT_stockInLtr];

                  
                obj_frm_TBL_DEPARTMENTS.DBStatus = 'U';
                obj_frm_TBL_DEPARTMENTS.CheckEdit_Is_AutoGenegereted.Enabled = false;
            }
            catch (Exception ex)
            {

                return ex.Message;

            }

            return "ok";

        }


