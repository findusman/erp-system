        public bool validate(out string value, bool pIs_Delete)
        {
            if (pIs_Delete)
            {

                if (!isAllowedDeletion)
                {
                    value = obj_cls_MessageBox.error_delete_rights;
                    return false;

                }

                if (obj_frm_TBL_DEPARTMENTS.DBStatus == 'I')
                {
                    value = obj_cls_MessageBox.error_selection_msg;
                    return false;
                }

            }
            else
            {
                if (obj_frm_TBL_DEPARTMENTS.DBStatus == 'I')
                {
                    if (!isAllowedInsertion)
                    {
                        value = obj_cls_MessageBox.error_insert_rights;
                        return false;

                    }

                }
                if (obj_frm_TBL_DEPARTMENTS.DBStatus == 'U')
                {
                    if (!isAllowedUpdation)
                    {
                        value = obj_cls_MessageBox.error_update_rights;
                        return false;

                    }

                }

                if (Convert.ToString(obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_name.EditValue) == "")
                {
                     value = obj_cls_MessageBox.error_Name_msg;
                    obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_name.Focus();
                    obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_name.Properties.AppearanceFocused.BorderColor = System.Drawing.Color.Red;
                    return false;
                }
                if (Convert.ToString(obj_frm_TBL_DEPARTMENTS.GridLookUpEdit_DEPARTMENT_parentID.EditValue) == "" && obj_frm_TBL_DEPARTMENTS.checkEdit_DEPARTMENT_isParent.Checked)
                {
                      value = "V_P";
                      obj_frm_TBL_DEPARTMENTS.GridLookUpEdit_DEPARTMENT_parentID.Focus();
                      obj_frm_TBL_DEPARTMENTS.GridLookUpEdit_DEPARTMENT_parentID.Properties.AppearanceFocused.BorderColor = System.Drawing.Color.Red;
                      return false;
                }
            }

            if (obj_frm_TBL_DEPARTMENTS.DBStatus == 'U')
                if (Convert.ToString(obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_ID.EditValue) == "")
                {
                    value = obj_cls_MessageBox.error_ID_msg;
                    return false;
                }
            if (obj_frm_TBL_DEPARTMENTS.DBStatus == 'I')
                if (!obj_frm_TBL_DEPARTMENTS.CheckEdit_Is_AutoGenegereted.Checked && Convert.ToString(obj_frm_TBL_DEPARTMENTS.TextEdit_DEPARTMENT_ID.EditValue) == "")
                {
                    value = obj_cls_MessageBox.error_ID_msg;
                    return false;
                }


            value = "N";
            return true;
        }




