        public void selection(string pStatus, string pId)
        {

            if (pStatus == "N")
            {
                if (pId == "")
                {
                    obj_cls_DataSet_IMS.f_TBL_PACKINGS_MAIN("L", pId);
                    DataSet ds = obj_cls_DataSet_IMS.g_TBL_PACKINGS_MAIN;
                    if (ds == null)
                    {
                        obj_cls_MessageBox.MessageBoxStatic("BLL_E");
                        Referesh("False");
                        return;

                    }

                    dt_Datanavigator = ds.Tables[0].Copy();
                    obj_frm_TBL_PACKINGS_MAIN.DataNavigator_Navigate.DataSource = dt_Datanavigator;
                }
                else
                {

                    string ID = dt_Datanavigator.Rows[Convert.ToInt32(pId)][0].ToString();
                    selection("V", ID);
                }
            }
            else if (pStatus == "A")
            {

                obj_cls_DataSet_IMS.f_TBL_PACKINGS_MAIN("A", pId);
                DataSet ds = obj_cls_DataSet_IMS.g_TBL_PACKINGS_MAIN;

                if (ds == null)
                {
                    obj_cls_MessageBox.MessageBoxStatic("BLL_E");
                    Referesh("False");
                    return;

                }


                obj_frlLOOKUP = new GEN.GEN_GEN.Look.frlLOOKUP(ds, 4, true, "150,150,150,150", 1);
                obj_frlLOOKUP.ShowDialog();
                string value = GEN.GEN_GEN.Look.frlLOOKUP.value;
                if (value != "")
                {
                    string[] cols_values = value.Split('|');
                    obj_frm_TBL_PACKINGS_MAIN.CheckEdit_navigate.Checked = false;
                    selection("V", cols_values[0]);

                }

            }
            else
            {

                obj_frm_TBL_PACKINGS_MAIN.maxID = obj_frm_TBL_PACKINGS_MAIN.TextEdit_PACKING_MAIN_ID.Text = pId;
                obj_cls_DataSet_IMS.f_TBL_PACKINGS_MAIN("V", pId);
                obj_cls_DataSet_IMS.f_TBL_PACKINGS_DEF("V", pId);
                DataSet ds = obj_cls_DataSet_IMS.g_TBL_PACKINGS_MAIN;
                DataSet ds_def = obj_cls_DataSet_IMS.g_TBL_PACKINGS_DEF;
                string executeFunctions = "";

                if (!GEN.GEN_GEN.GenericClasses.DataTables.cls_NativDataSet.checkIsNullIsNoTableIsTableEmpty(ds, 0))
                {
                    obj_frm_TBL_PACKINGS_MAIN.DtpackingDef.Rows.Clear();
                    if (ds_def != null)
                    {
                        DataTable dd = ds_def.Tables[0];
                        foreach(DataRow dr in dd.Rows)
                        {
                            obj_frm_TBL_PACKINGS_MAIN.DtpackingDef.Rows.Add(dr[0],dr[1],dr[2],dr[3],dr[4]);
                        }
                    }
                    
                   
                    executeFunctions = valueSetting(ds);
                }
                else
                {

                    obj_cls_MessageBox.MessageBoxStatic("TB_E");
                    Referesh("False");
                    return;

                }


                if (executeFunctions != "ok")
                {
                    obj_cls_MessageBox.MessageBoxDynamics(executeFunctions, "S_E");
                    return;

                    Referesh("False");


                }


            }

        }


